class FlatIterator:

    def __init__(self, list_of_list):

        self.list_of_list = list_of_list
        self.result = []
        self.list_unpack(list_of_list)

    def list_unpack(self, list_of_list):

        for item in list_of_list:
            if isinstance(item, list):  # если элемент главного списка - список:
                self.list_unpack(item)  # рекурсивно вызываем метод для вложенных списков
            else:
                self.result.append(item)

    def __iter__(self):
        self.counter = 0  # счетчик
        return self

    def __next__(self):

        if self.counter == len(self.result):
            raise StopIteration
        else:
            item = self.result[self.counter]
            self.counter += 1
            return item



def test_1():

    list_of_lists_1 = [
        ['a', 'b', 'c'],
        ['d', 'e', 'f', 'h', False],
        [1, 2, None]
    ]

    for flat_iterator_item, check_item in zip(
            FlatIterator(list_of_lists_1),
            ['a', 'b', 'c', 'd', 'e', 'f', 'h', False, 1, 2, None]
    ):

        assert flat_iterator_item == check_item
    assert list(FlatIterator(list_of_lists_1)) == ['a', 'b', 'c', 'd', 'e', 'f', 'h', False, 1, 2, None]


if __name__ == '__main__':
    test_1()





